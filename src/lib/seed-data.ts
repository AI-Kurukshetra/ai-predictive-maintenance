function createTelemetry(
  equipmentId: string,
  vibration: number[],
  temperature: number[],
  acoustic: number[],
  pressure: number[],
  runtimeStart: number,
): EquipmentTelemetry {
  return {
    equipmentId,
    points: seriesTimestamps.map((timestamp, index) => ({
      timestamp,
      vibration: vibration[index],
      temperature: temperature[index],
      acoustic: acoustic[index],
      pressure: pressure[index],
      runtimeHours: runtimeStart + index * 4,
    })),
  };
}

